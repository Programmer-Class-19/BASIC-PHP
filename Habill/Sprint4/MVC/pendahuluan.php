Model : data
- representasi pengetahuan
- mengelola data
- logika bisnis

Controller : proses 
- perantara antara model dan view
- menangani pemrosesan pada aplikasi
- menangani aksi dari user

View : tampilan
- output, apa yang dilihat oleh user
- representasi visual dari model
- lapisan representasi

kenapa MVC?
- organisasi dan struktur kode yang baik
- pemisahan logic dan tmapilan
- memudahkan untuk maintenance (perawatan) kode
- supaya dapat gambaran untuk meng implementasi konsep OOP yang sudah dipelajari
